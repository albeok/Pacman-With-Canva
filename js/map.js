const foods=[];
const powerPills=[];
const boundaries=[];
let door;

map.forEach((row,i)=>{
	row.forEach((symbol,j)=>{
		switch(symbol){
			case ' ':
			case 'B':
				break;
			case '.':
				foods.push(
					new Food({
						_position:{
							x:Boundary.distance*j,
							y:Boundary.distance*i
						}					
					})
				);
				break;
			case 'o':
				powerPills.push(
					new PowerPill({
						_position:{
							x:Boundary.distance*j,
							y:Boundary.distance*i
						}
					})
				);
				break;
			case '_':
				door=new Door(
					{
						_position:{
							x:Boundary.distance*j,
							y:Boundary.distance*i
						}
					}
				);
				break;
			default:
				boundaries.push(
					new Boundary({
						_position:{
							x:Boundary.distance*j,
							y:Boundary.distance*i
						}
					},symbol)
				);
				break;
		}
	});
});

const drawOnMap=()=>{
	boundaries.forEach(boundary=>{
		boundary.draw();
	});
	
	door.draw();
	
	foods.forEach(food=>{
		food.draw();
	});
	
	powerPills.forEach(pill=>{
		pill.draw();
	});
};